def suma(a, b): # Definimos la función "suma" • Tiene 2 parámetros.
    return a+b # "return" devuelve el resultado de la función.

resultado_entero = suma(2, 3) # Función con ints
print(resultado_entero)

# Resultado = 5

resultado_decimal = suma (2.7, 4.0) # Función con floats
print(resultado_decimal)
# Resultado = 6.7

resultado_cadena = suma ('Me gusta', 'Python') # Función con strings
print(resultado_cadena)